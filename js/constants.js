function getLevelTheme(level) {
  return LEVEL_THEMES[(level - 1) % LEVEL_THEMES.length];
}

function getEnemyCount(level) {
  if (level <= 2) return 3;
  if (level <= 4) return 4;
  return 5;
}

function getEnemyPositions(level) {
  return ALL_ENEMY_POSITIONS.slice(0, getEnemyCount(level));
}

